  <p>3.1.1.13.  Нести расходы по оплате парковочных мест, документально подтвержденные Агентом, при условии, что указанные расходы непосредственно связаны с исполнением Заказов и включены в Объем выполненных Заказов дополнительно к тарифу на перевозки.</p>
