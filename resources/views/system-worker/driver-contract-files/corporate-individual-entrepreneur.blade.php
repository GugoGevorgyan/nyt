  <p>3.1.1.7. Нести расходы в связи с ремонтом и эксплуатацией Дополнительного оборудования, за исключением случаев, когда необходимость ремонта Дополнительного оборудования вызвана ненадлежащей его эксплуатацией Агентом;</p>
