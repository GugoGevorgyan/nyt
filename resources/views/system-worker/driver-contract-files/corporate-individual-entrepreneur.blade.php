      <li>при эксплуатации Транспортного средства, как в Дни выезда, так и в любые иные дни  использовать исключительно автомобильное топливо, полученное наливом, в порядке и на условиях, установленных пунктами 5.1. –  5.4. настоящего Договора;</li>
      <li>осуществлять эксплуатацию Транспортного средства и Дополнительного оборудования исключительно в соответствии с настоящим Договором.</li>
    </ul>
  </p>
  <p>3.3.1.4. Обеспечить сохранность и комплектность полученного Транспортного средства и Дополнительного оборудования;</p>
  <p>3.3.1.5. Обеспечить сохранность переданных Принципалом  регистрационных и иных документов, необходимых Агенту для использования Транспортного средства в режиме такси;</p>
  <p>3.3.1.6. Обеспечивать безопасную эксплуатацию Транспортного средства;</p>
  <p>3.3.1.7. Надлежащим образом следить за внешним видом и техническим состоянием Транспортного средства;</p>
  <p>3.3.1.8. В случае выявления технических неисправности Транспортного средства незамедлительно обеспечить доставку автомобиля в ремонтную зону Принципала, согласовав с Принципалом адрес подачи;</p>
  <p>3.3.1.9. Для своевременного прохождения планового государственного технического осмотра, размещения на транспортном средстве рекламных изображений, а также в иных случаях  предоставить Транспортное средство лицу, указному Принципалом,  по адресу и во время,  указанные Принципалом;</p>
  <p>3.3.1.10. При выполнении Заказов Принципала соблюдать Правила перевозки пассажиров  ООО «Новая транспортная компания», установленные Приложением № 3 к настоящему Договору с учетом всех изменений и дополнений, вносимых в указанные правила в соответствии с настоящим Договором;</p>
  <p>3.3.1.11. Принимать и выполнять все Заказы Принципала по мере их поступления  не менее 22 дней в каждом полном календарном месяце срока действия настоящего Договора, в соответствии с графиком, согласованным с Принципалом в соответствии с пунктом 3.3.1.13.настоящего договора;</p>
  <p>3.3.1.12. Не позднее, чем за пять дней до начала каждого календарного месяца  согласовывать с Принципалом график Дней-выезда на календарный месяц (время и дни в календарном месяце, в которые Агент обязуется принимать и выполнять Заказы Принципала), за исключением первого календарного месяца срока действия настоящего Договора.</p>
  <p>При этом Агент обязуется согласовать график Дней-выезда на первый календарный месяц в течение 2 (двух) дней с момента заключения настоящего Договора;</p>
  <p>3.3.1.13. Незамедлительно после выполнения Заказа (после окончания перевозки пассажира (пассажиров)):
