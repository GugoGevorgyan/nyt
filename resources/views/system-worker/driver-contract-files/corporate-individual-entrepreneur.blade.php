  <p>3.1.1.4. При передаче Заказа давать Агенту указания, необходимые для выполнения Заказа, в том числе: ФИО пассажира (наименование организации), адрес подачи автомобиля (пункт отправления), предполагаемое время начала перевозки;</p>
