<p>5.1. Водитель ежедневно проходит медицинский контроль </p>
<p>5.2. Одежда водителя должна быть чистой и опрятной. Спортивный костюм, шорты и майки не допустимы.</p>
<p>5.2. Одежда водителя должна быть чистой и опрятной. Спортивный костюм, шорты и майки не допустимы
