<p>6.4. Водитель должен помочь клиенту поместить в такси багаж, если таковой у него имеется.</p>
<p>6.5. Провоз животных разрешается при следующих условиях: для собак необходим намордник и подстилка, кошки перевозятся в клетке или в специальной сумке.</p>
