  <p>3.2.1.2. Принципал вправе в одностороннем порядке вносить изменения в Правила перевозки пассажиров  ООО «Новая транспортная компания», установленные Приложением  № 3 к настоящему Договору, которые будут, по его мнению, необходимы для осуществления  Агентом перевозки надлежащим образом.</p>
  <h5>3.3. Обязанности Агента</h5>
  <p>3.3.1. Агент обязуется:</p>
