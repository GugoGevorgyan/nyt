<p>1.2. Возможно опоздание такси до 15 мин., при этом водитель заранее сообщает в РДЦ о времени опоздания.</p>
<h5>2. Расчет водителя с клиентом</h5>
<p>2.1. {{$entity->type? $entity->type->abbreviation: ''}} «{{$entity->name}}» предоставляет своим клиентам транспортные услуги, при этом:
<ul>
  <li>оплата производится строго по таксометру, не допускается округление суммы заказа;</li>
  <li>водитель не требует у клиента чаевые.</li>
</ul>
</p>
<p>2.2. Информация о тарифах на перевозку пассажиров - наклейки, расположенные на передней панели транспортного средства и на правой задней двери транспортного средства, с тарифами.</p>
<h5>3. Знание водителем Москвы</h5>
<p>3.1. Водитель должен знать основные улицы, магистрали, транспортные развязки, аэропорты и ж/д вокзалы.</p>
<p>3.2. Перед подачей автомобиля клиенту водитель должен иметь представление о маршруте поездки, при этом он может использовать атласы дорог.</p>
<p>3.3. Перед началом поездки клиент вправе выбрать маршрут поездки (водитель должен уточнить маршрут у клиента).</p>
<h5>4. Техническое состояние и чистота автомобиля</h5>
<p>4.1. Автомобиль проходит ежедневную мойку.</p>
<p>4.2. Внутри салона не должно находиться посторонних предметов.</p>
<p>4.3. В салоне должно быть чисто, не накурено.</p>
<p>4.4. Если происходит поломка или авария автомобиля в пределах МКАД, клиент оплачивает данную поездку согласно показаниям таксометра, при этом клиенту предоставляется в течение  1 часа другой автомобиль.</p>
