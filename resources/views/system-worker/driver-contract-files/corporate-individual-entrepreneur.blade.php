  <p>3.3.1.3. Обеспечить надлежащую эксплуатацию Транспортного средства и Дополнительного оборудования, а именно:
    <ul>
      <li>осуществлять эксплуатацию Транспортного средства и Дополнительного оборудования в строгом соответствии с технической документацией, регламентирующей порядок эксплуатации Транспортного средства и Дополнительно оборудования, переданной Принципалом, в том числе в соответствии с руководством по эксплуатации Транспортного средства;</li>
