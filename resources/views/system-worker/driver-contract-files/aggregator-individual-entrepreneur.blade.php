  </li>
  <li>в случае некорректного поведения Агента по отношению к пассажиру - штраф в размере 6 000 руб.;</li>
  <li>в случае неопрятного внешнего вида Агента при выполнении им своих обязанностей по перевозке пассажиров - штраф в
    размере 3 000 руб.;
  </li>
  <li>в случае не соблюдения Агентом графика Дней-выезда - штраф в размере 3000 руб.;</li>
</ul>
</p>
