<h4 style="text-align: center">1. ПРИМЕНИМЫЕ ТЕРМИНЫ</h4>
<p>1.1. Для целей настоящего Договора нижеприведенные термины, если контекст и содержание настоящего Договора не требуют
  иного, имеют следующее значение:</p>
<p><strong>Заказ</strong> – означает поручение Принципала Агенту заключить договор перевозки и осуществить перевозку
  пассажира (пассажиров), переданное Агенту Принципалом посредством навигатора, установленной в Транспортном средстве,
  или переданное Агенту Принципалом иным способом. По соглашению Сторон Заказ считается выполненным Агентом при
  одновременном выполнении следующих условий:
<ul>
  <li>перевозка пассажира (пассажиров) Агентом осуществлена полностью;</li>
  <li>Агентом получена от пассажира сумма денежных средств, составляющая стоимость перевозки.</li>
</ul>
</p>
<p><strong>День выезда</strong> – означает каждый календарный день, в который Агент выполнил один или более Заказов
  Принципала. Количество Дней выезда определяется на основании графика, согласованного с Принципалом.</p>
<p><strong>Неполный календарный месяц</strong> – означает:
<ul>
  <li>период, который начинается с даты заключения настоящего Договора и заканчивается в последний день календарного
    месяца, на который приходится дата заключения Договора;
  </li>
  <li>период, который начинается в первый день календарного месяца, на который приходится дата окончания срока действия
    настоящего Договора, и заканчивается в дату окончания срока действия настоящего Договора;
  </li>
  <li>любой календарный месяц, в случае, если в таком месяце Агент воспользовался правом приостановить принятие и (или)
    исполнение Заказов Принципала в соответствии с настоящим Договором.
  </li>
</ul>
</p>
<h4 style="text-align: center">2. ПРЕДМЕТ ДОГОВОРА</h4>
<p>2.1. По настоящему Договору Агент обязуется за вознаграждение Принципала выполнять Заказы Принципала в порядке и на
  условиях, предусмотренных настоящим Договором.</p>
<h4 style="text-align: center">3. ПРАВА И ОБЯЗАННОСТИ СТОРОН</h4>
<h5>3.1. Обязанности Принципала.</h5>
<p>3.1.1. Принципал обязуется:</p>
<p>3.1.1.1. Передавать Агенту Заказы посредством навигатора, установленной в Транспортном средстве, либо иным
  способом;</p>
<p>3.1.1.2. При передаче Заказа давать Агенту указания, необходимые для выполнения Заказа, в том числе: ФИО пассажира
  (наименование организации), адрес подачи автомобиля (пункт отправления), предполагаемое время начала перевозки;</p>
<h5>3.2. Права Принципала</h5>
<p>3.2.1. Принципал вправе:</p>
<p>3.2.1.1. Принципал вправе в любое время без согласования с Агентом осуществлять контроль за выполнением Агентом
  поручений (Заявок) Принципала.</p>
<p>3.2.1.2. Принципал вправе в одностороннем порядке вносить изменения в Правила перевозки пассажиров ООО «Новая
  транспортная компания», установленные Приложением № 1 к настоящему Договору, которые будут, по его мнению, необходимы
  для осуществления Агентом перевозки надлежащим образом.</p>
<h5>3.3. Обязанности Агента</h5>
<p>3.3.1. Агент обязуется:</p>
<p>3.3.1.1. При выполнении Заказов Принципала соблюдать Правила перевозки пассажиров ООО «Новая транспортная компания»,
  установленные Приложением № 1 к настоящему Договору с учетом всех изменений и дополнений, вносимых в указанные
  правила;</p>
<P>3.3.1.2. Принимать и выполнять все Заказы Принципала по мере их поступления не менее 22 дней в каждом полном
  календарном месяце срока действия настоящего Договора, в соответствии с графиком, согласованным с Принципалом;</P>
<p>3.3.1.3. Принимать и выполнять все Заказы Принципала по мере их поступления в Неполный первый и (или) последний
  календарный месяц не менее количества дней, определяемого как произведение коэффициента 0,722 (ноль целых семьсот
  двадцать две тысячных) и количества дней в Неполном календарном месяце, округленное до целого числа (в большую
  сторону), в соответствии с графиком, согласованным с Принципалом за исключением случаев, когда Агент воспользовался
  правом приостановить принятие и (или) выполнение Заказов Принципала в соответствии с настоящим Договором;</p>
<p>3.3.1.4. Не позднее, чем за пять дней до начала каждого календарного месяца согласовывать с Принципалом график
  Дней-выезда на календарный месяц (время и дни в календарном месяце, в которые Агент обязуется принимать и выполнять
  Заказы Принципала), за исключением первого календарного месяца срока действия настоящего Договора.</p>
<p>При этом Агент обязуется согласовать график Дней-выезда на первый календарный месяц в течение 2 (двух) дней с момента
  заключения настоящего Договора;</p>
<p>3.3.1.5. При определении стоимости перевозки пассажира (пассажиров) при выполнении Заказа руководствоваться тарифами
  на перевозки, установленными Принципалом;</p>
<p>3.3.1.6. Лично выполнять Заказы Принципала;</p>
<p>3.3.1.7. Сообщать Принципалу, по его требованию, все сведения о ходе выполнения Заказа;</p>
<p>3.3.1.8. Не заключать с другими принципалами аналогичных агентских договоров;</p>
<p>3.3.1.9. Незамедлительно уведомить Принципала при прекращении права осуществлять деятельность в качестве
  индивидуального предпринимателя.</p>
<p>3.3.1.10. При использовании транспортного средства, принадлежащего Агенту, для выполнении заказов принципала,
  получить и иметь всегда с собой разрешение на таксомоторную деятельность в г.Москве.</p>
<p>3.3.1.11. При использовании транспортного средства, принадлежащего третьему лицу, для выполнении заказов принципала
  иметь всегда с собой разрешение от собственника (третьего лица) на таксомоторную деятельность в г.Москве.</p>
<h5>3.4. Права Агента.</h5>
<p>3.4.1. Агент вправе:</p>
<p>3.4.1.1. Приостановить принятие и (или) выполнение Заказов Принципала, в случае:
<ol>
  <li>болезни Агента, либо его детей, смерти родственников (родителей, супруги (а), детей), незамедлительно уведомив об
    этом Принципала;
  </li>
  <li>по любой иной причине, в том числе в случае свадьбы Агента, либо его детей, предварительно согласовав с
    Принципалом даты приостановления принятия Заказов Принципала не менее чем за 30 календарных дней до предполагаемой
    даты приостановления прав и обязанностей,
