<p>9.1. Стороны освобождаются от ответственности за частичное или полное неисполнение обязательств по настоящему
  Договору, если это неисполнение явилось следствием обстоятельств непреодолимой силы, возникших после заключения
  настоящего Договора, в результате таких событий чрезвычайного характера, которые Стороны не могли предвидеть или
  предотвратить разумными мерами. К обстоятельствам непреодолимой силы относятся события, на которые Стороны не могут
  оказать влияние и за возникновение которых они не несут ответственности, в том числе землетрясение, наводнение, иные
  стихийные бедствия, а также забастовка, военные действия, правовые акты органов государственной власти.</p>
<p>9.2. Сторона, для которой создалась невозможность исполнения обязательств по настоящему Договору, обязана известить в
