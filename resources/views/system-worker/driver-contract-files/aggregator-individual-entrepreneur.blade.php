<h4 style="text-align: center">6. ОТВЕТСТВЕННОСТЬ СТОРОН</h4>
<p>6.1. За неисполнение или ненадлежащее исполнение обязательств по договору Принципал и Агент несут ответственность в
  соответствии с действующим законодательством РФ.</p>
<p>6.2. В случае неисполнения или ненадлежащего исполнения Агентом обязательства по незамедлительному уведомлению
  Принципала в случае прекращении права Агента осуществлять деятельность в качестве индивидуального предпринимателя
