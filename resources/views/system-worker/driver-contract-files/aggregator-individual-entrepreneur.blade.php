<p>При этом для целей пункта 3.4.1.1. настоящего Договора:
<ul>
  <li>первый полный год срока действия настоящего Договора начинается в дату заключения настоящего Договора и
    оканчивается по истечении 12 календарных месяцев в последний день последнего месяца, за исключением случаев, когда
    настоящий Договор заключен менее чем на год;
  </li>
  <li>каждый последующий полный год срока действия настоящего Договора начинается в день, следующий за днем окончания
    первого года срока действия настоящего Договора, и оканчивается по истечении 12 календарных месяцев в последний день
    последнего месяца;
  </li>
  <li>каждый неполный год срока действия настоящего Договора начинается в день следующий за днем окончания предыдущего
    полного года срока действия настоящего Договора и оканчивается в дату окончания действия настоящего Договора, за
    исключением случаев, когда настоящий Договор заключен на срок менее года в указанном случае неполный год срока
    действия настоящего Договора начинается в дату заключения настоящего договора и оканчивается в дату окончания
    действия настоящего Договора.
  </li>
</ul>
</p>
<p>3.4.1.2. В случае надлежащего исполнения обязательств, предусмотренных настоящим Договором, после окончания действия
  настоящего Договора Агент вправе заключить с Принципалом аналогичный договор на новый срок с предоставлением
  Принципалом Агенту иного транспортного средства.</p>
<p>3.4.1.3. Агент не вправе в целях исполнения настоящего Договора заключить субагентский договор с другим лицом.</p>
