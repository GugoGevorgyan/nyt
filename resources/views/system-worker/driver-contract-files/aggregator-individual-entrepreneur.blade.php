  ненадлежащего исполнения указанного обязательства, а также возместить Принципалу причиненные убытки в течении одного
  дня с момента предъявления Принципалом соответствующих требований, при этом указанные убытки могут быть взысканы
  Принципалом сверх указанного штрафа.</p>
<p>6.3. В случае ненадлежащего исполнения Агентом обязательств по Договору Агент обязуется уплатить штрафы в следующих
  размерах и случаях:
<ul>
  <li>в случае неверного расчета Агентом стоимости перевозки - штраф в размере, определяемом как увеличенная в три раза
    разница между стоимостью перевозки, фактически рассчитанной Агентом, и стоимостью перевозки, рассчитанной исходя из
    тарифов, установленных Принципалом, но не менее 10 000 руб. по каждому случаю неверного расчета;
  </li>
  <li>в случае отказа Агента от выполнения Заказа - штраф в размере 6000 руб.;</li>
  <li>в случае срыва выполнения Заказа, принятого Агентом и происшедшего по его вине - штраф в размере 6 000 руб.;</li>
  <li>в случае нарушения Агентом правил расчета с пассажиром, выразившегося в отсутствии у Агента суммы наличных
